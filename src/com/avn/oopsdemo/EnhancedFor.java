package com.avn.oopsdemo;

public class EnhancedFor {
	// Method to demonstrate enhanced for loop
    void displayNumbers() {
        int[] arr = {1, 2, 3, 4, 5};

        System.out.println("=== Enhanced For Loop Example ===");

        // Using enhanced for loop to print array elements
        for (int num : arr) {
            System.out.println(num);
        }
    }

}
